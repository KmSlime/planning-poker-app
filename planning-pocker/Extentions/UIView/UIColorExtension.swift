import UIKit

extension UIColor {
    class func primaryColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 170.0/255.0, blue: 231.0/255.0, alpha: 1.0)
    }
    class func primaryLightColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 170.0/255.0, blue: 231.0/255.0, alpha: 0.15)
    }
    class func secondaryColor() -> UIColor {
        return UIColor(red: 57.0/255.0, green: 147.0/255.0, blue: 255.0/255.0, alpha: 0.12)
    }
    class func grayColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 0.0/255.0, blue: 0.0/255.0, alpha: 0.24)
    }
    class func grayTextBoldColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 0.0/255.0, blue: 0.0/255.0, alpha: 0.64)
    }
    class func grayTextMediumColor() -> UIColor {
        return UIColor(red: 136.0/255.0, green: 136.0/255.0, blue: 136.0/255.0, alpha: 1)
    }
    
}
